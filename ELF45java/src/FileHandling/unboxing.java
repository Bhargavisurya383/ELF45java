package FileHandling;

public class unboxing {
	public static void main(String[] args) {
		Integer a=10;
		int i=a.intValue();
		System.out.println(i);
		
		Character ch='&';
		char c=ch.charValue();
		System.out.println(c);
		
		Double d1=23.98;
		double d=d1.doubleValue();
		System.out.println(d);
	}

}
