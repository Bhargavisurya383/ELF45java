package String_Class;

public class Demo13 {
	public static void main(String[] args) {
		String s="Harry";
		System.out.println(s.indexOf('a'));//1
		System.out.println(s.indexOf('H'));//0
		System.out.println(s.indexOf('r'));//2
		System.out.println(s.indexOf("arry"));//1
		System.out.println(s.indexOf("Harry"));//0
		System.out.println(s.lastIndexOf('r'));//3
	
		
		
	}

}
